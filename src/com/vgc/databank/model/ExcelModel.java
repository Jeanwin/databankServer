package com.vgc.databank.model;

import java.util.List;

/**
 * record excel some info
 * 
 * @author BULL
 */
public class ExcelModel {
	/**
	 * sheet name
	 * default value in conf file:Implemented 
	 */
	private String sheetName;
	/**
	 * sheet title
	 * default value in conf file:Design Guideline regarding Technical Standards 
	 * and Legislations in China 
	 */
	private String sheetTitle;
	/**
	 * item selected title
	 * default value in conf file:Items Information
	 */
	private String selectedItemTitle;
	/**
	 * data summary title
	 * default value in conf file:Design Guideline
	 */
	private String dataSummaryTitle;
	/**
	 * default font
	 * default value in conf file:Arial
	 */
	private String defaultFont;
	
	public String getDefaultFont() {
		return defaultFont;
	}
	/**
	 * set default font 
	 * 
	 * @param defaultFont
	 */
	public void setDefaultFont(String defaultFont) {
		this.defaultFont = defaultFont;
	}

	public String getSheetTitle() {
		return sheetTitle;
	}
	/**
	 * set sheet title
	 * 
	 * @param sheetTitle
	 */
	public void setSheetTitle(String sheetTitle) {
		this.sheetTitle = sheetTitle;
	}
	
	public String getSelectedItemTitle() {
		return selectedItemTitle;
	}
	/**
	 * set item selected title
	 * 
	 * @return
	 */
	public void setSelectedItemTitle(String selectedItemTitle) {
		this.selectedItemTitle = selectedItemTitle;
	}

	public String getDataSummaryTitle() {
		return dataSummaryTitle;
	}
	/**
	 * set data summary title
	 * 
	 * @param dataSummaryTitle
	 */
	public void setDataSummaryTitle(String dataSummaryTitle) {
		this.dataSummaryTitle = dataSummaryTitle;
	}

	private int sumColumnNum;
	
	private int columnSize;
	
	List<ExcelTitle> titles = null;

	public String getSheetName() {
		return sheetName;
	}
	/**
	 * set sheet name
	 * 
	 * @param sheetName
	 */
	public void setSheetName(String sheetName) {
		this.sheetName = sheetName;
	}
	
	public int getSumColumnNum() {
		return sumColumnNum;
	}
	
	public void setSumColumnNum(int sumColumnNum) {
		this.sumColumnNum = sumColumnNum;
	}
	
	public int getColumnSize() {
		return columnSize;
	}

	public void setColumnSize(int columnSize) {
		this.columnSize = columnSize;
	}

	public List<ExcelTitle> getTitles() {
		return titles;
	}

	public void setTitles(List<ExcelTitle> titles) {
		this.titles = titles;
	}
	
	
}
